/**
 * The concat() method is used to merge two or more arrays. This method does not change the existing arrays, but instead returns a new array.
 */
var arr1 = ["a", "b", "c"];
var arr2 = ["d", "e", "f"];

let res = arr1.concat(arr2);

console.log(res);
// results in a new array [ "a", "b", "c", "d", "e", "f" ]
